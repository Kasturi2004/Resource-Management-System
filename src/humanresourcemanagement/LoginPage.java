    private void bloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bloginActionPerformed
        // TODO add your handling code here:
        Connection conn = DatabaseConnection.connect(); // Get the connection from DatabaseConnection class

try {
    if (conn != null) {
        // Prepare the SQL query
        String query = "SELECT * FROM logins WHERE (email = ? OR username = ?) AND password = ?";
        PreparedStatement pstmt = conn.prepareStatement(query);

        // Get email and password from text fields
        String emailOrUsername = this.jtuser.getText(); // Assuming you have a JTextField for email
        String password = new String(jpassword.getPassword()); // Assuming you have a JPasswordField for password

        // Set the parameters for the prepared statement
        pstmt.setString(1, emailOrUsername);
        pstmt.setString(2, emailOrUsername);
        pstmt.setString(3, password);

        // Execute the query
        ResultSet rs = pstmt.executeQuery();

        // Process the result set
        if (rs.next()) {
            // User found
            String role = rs.getString("role"); // Assuming 'role' is the column name in your logins table
            System.out.println("User found: " + rs.getString("email") + " with role: " + role);

            // Open the appropriate JFrame based on the role
            if ("manager".equalsIgnoreCase(role)) {
                OptionPage2 OptionPage2Frame = new OptionPage2();
                OptionPage2Frame.setVisible(true);
                OptionPage2Frame.pack();
                OptionPage2Frame.setLocationRelativeTo(null);
            } else if ("employee".equalsIgnoreCase(role)) {
                OptionPage1 OptionPage1Frame = new OptionPage1();
                OptionPage1Frame.setVisible(true);
                OptionPage1Frame.pack();
                OptionPage1Frame.setLocationRelativeTo(null);
            }

            this.dispose(); // Close the login frame
        } else {
            // User not found
            System.out.println("User not found or invalid credentials.");
            JOptionPane.showMessageDialog(null, "Wrong Username or Password!!!", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }
} catch (SQLException e) {
    e.printStackTrace(); // Print any SQL exceptions that occur
} finally {
    // Always close the connection after use
    if (conn != null) {
        try {
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace(); // Print any exceptions when closing the connection
        }
    }
}
    }//GEN-LAST:event_bloginActionPerformed

